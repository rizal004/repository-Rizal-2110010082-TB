    private void bt_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_cariActionPerformed
       try {
    java.sql.Connection conn=(java.sql.Connection)project.Koneksi.koneksiDB();
    String sql = "Select * from tborangtua where noktp='"+cari.getText()+"' or nama='"+cari.getText()+"'";
    java.sql.PreparedStatement pst = conn.prepareStatement(sql);
    ResultSet rs = pst.executeQuery(sql);
    if (rs.next()){
        noktp.setText(rs.getString(1));
        nama.setText(rs.getString(2));
        jeniskelamin.setSelectedItem(rs.getString(3));
        tempat.setText(rs.getString(4));
        tgl.setDate(rs.getDate(5));
        alamat.setText(rs.getString(6));
        JOptionPane.showMessageDialog(null,"Data tborangtua : "
        + cari.getText() + " ditemukan, Apakah Anda Ingin Mengedit atau Menghapus?");
    }
} catch (Exception e){}
cari.requestFocus();
    }//GEN-LAST:event_bt_cariActionPerformed
    
    public void tampil_data(){
        DefaultTableModel tabel= new DefaultTableModel();// Membuat objek DefaultTableModel untuk menampung data tabel
        tabel.addColumn("NOKTP");// Menambahkan kolom-kolom yang akan ditampilkan dalam tabel
        tabel.addColumn("NAMA");
        tabel.addColumn("JENISKELAMIN");
        tabel.addColumn("TEMPAT");
        tabel.addColumn("TGL");
        tabel.addColumn("ALAMAT");

        try{
            java.sql.Connection conn=(java.sql.Connection)project.Koneksi.koneksiDB();
            String sql = "select *from tborangtua";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery(sql);
            while(rs.next())
            {
               tabel.addRow(new Object[]{ //Mengambil baris demi baris dari ResultSet dan menambahkannya ke objek tabel
                   rs.getString(1),
                   rs.getString(2),
                   rs.getString(3),
                   rs.getString(4),
                   rs.getString(5),
                   rs.getString(6),});
                      
            }
        jTable1.setModel(tabel);// Menetapkan model tabel (tabel yang sudah berisi data) ke komponen jTable1
        }catch (Exception e){
        
        }
    
    }
    
    
    
