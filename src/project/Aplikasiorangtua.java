    private void bt_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_simpanActionPerformed
      try{
          String sql = "insert into tborangtua values('"//simpan data pada tabel 'orangtua'
                  +noktp.getText()+"','"
                  +nama.getText()+"','"
                  +jeniskelamin.getSelectedItem()+"','"
                  +tempat.getText()+"','"
                  +tgl_lahir+"','"
                  +alamat.getText()+"')";
          java.sql.Connection conn = (java.sql.Connection)project.Koneksi.koneksiDB();// Mendapatkan koneksi ke database
          java.sql.PreparedStatement pst = conn.prepareStatement(sql);// Membuat objek PreparedStatement untuk mengeksekusi pernyataan SQL
                pst.execute();
                JOptionPane.showMessageDialog(null, "Terima Kasih ");// Menampilkan pesan bahwa data berhasil disimpan
                tampil_data();// Memanggil fungsi tampil_data() untuk memperbarui tampilan data
      }
      catch(Exception e){
          JOptionPane.showMessageDialog(null, "gagal disimpan");// Menampilkan pesan bahwa data gagal disimpan
          System.out.println(e.getMessage());// Menampilkan informasi kesalahan pada konsol
      }
    }//GEN-LAST:event_bt_simpanActionPerformed

